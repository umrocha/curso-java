package com.stefanini.aprendendocomeclipse.aula4;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		int soma = 3 + 7; // infix
		int subtração = 3 - 7;
		int divisão = 3 / 7;
		int multipica = 3 * 7;
		int modulo = 3 % 7;
		
		int a = 11;
		
		a++;//postfix
		++a;//prefix
		System.out.println(++a == 14);
	}

}
